my_str = 'ABHAY'

print(my_str.isalnum()) # returns True if all the characters are alphanumeric, meaning alphabet letter (a-z) and numbers (0-9)
print(my_str.isalpha()) # check if all char in the string are alphabetic
print(my_str.isdigit()) # test if string contains digits
print(my_str.istitle()) # test if string contains title words -> first letter capital
print(my_str.isupper()) # test if string is all upper case
print(my_str.islower()) # test if string is all lower case
print(my_str.isspace()) # returns True if all the characters in a string are whitespaces, otherwise False
print(my_str.endswith('a'))  # test if string endswith a d
print(my_str.startswith('A'))  # test if string startswith H
